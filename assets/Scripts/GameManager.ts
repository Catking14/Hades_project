    onLoad () 
    {
        let physics_manager = cc.director.getPhysicsManager();

        // enable physics function
        physics_manager.enabled = true;
        // physics_manager.debugDrawFlags = 1;

        // get cameras
        this._camera = cc.find("Canvas/Main Camera");
        this._minimap_camera = cc.find("Canvas/Minimap");

        // play BGM
        this._BGM = cc.audioEngine.playMusic(this.Megalovania, true);
        // cc.audioEngine.setMusicVolume(this.volume);
        // cc.audioEngine.setEffectsVolume(this.volume + 0.05);

        // create NodePool
        for (let i = 0; i < 3; i++)
        {
            this.monster_pool[i] = new cc.NodePool();
        }
        this._spawn_pool = new cc.NodePool();

        this.cur_scene = cc.find("Data").getComponent("Data").scene;
    }

    start () 
    {
        if(this.cur_scene != "BossSlime" && this.cur_scene != "BossBeholder")
        {
            this.generate_map();
            this.generate_transporter();
        }

        // generate selected player
        let p1;
        let player_role = cc.find("Data").getComponent("Data").role;

        if(player_role == "Wizard")
        {
            p1 = cc.instantiate(this.wizard);
        }
        else if(player_role == "Archor")
        {
            p1 = cc.instantiate(this.archor);
        }
        else if(player_role == "Assassin")
        {
            p1 = cc.instantiate(this.assassin);
        }
        else if(player_role == "Viking")
        {
            p1 = cc.instantiate(this.viking);
        }
        else
        {
            p1 = cc.instantiate(this.warrior);
        }

        this.follow = p1;
        if (this._obsiticles[4][4] == 0)
            p1.setPosition(4 * 32 - 440, 4 * 32 - 440);
        else
            p1.setPosition(15 * 32 - 480, 15 * 32 - 480);

        cc.find("Canvas/New Node").addChild(p1);

        this.schedule(() => {this.timer += 1}, 1);
    }

    update (dt) 
    {
        if(this.cur_scene != "BossSlime" && this.cur_scene != "BossBeholder")
        {
            this.camera_follow();
            this.generate_mobs_detect();
        }
    }
